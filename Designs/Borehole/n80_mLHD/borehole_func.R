# borehole function
bore.function <- function(x) {
  x[,1]<-x[,1]*(0.15-0.05)+0.05
  x[,2]<-x[,2]*(50000-100)+100
  x[,3]<-x[,3]*(115600-63070)+63070
  x[,4]<-x[,4]*(1110-990)+990
  x[,5]<-x[,5]*(116-63.1)+63.1
  x[,6]<-x[,6]*(820-700)+700
  x[,7]<-x[,7]*(1680-1120)+1120
  x[,8]<-x[,8]*(12045-9855)+9855
  
  nume<-2*pi*x[,3]*(x[,4]-x[,6])
  tem<-log(x[,2]/x[,1])
  den<-tem*(1+(2*x[,7]*x[,3])/(tem*(x[,1])^2*x[,8])+x[,3]/x[,5])
  y<-nume/den
  y<-as.numeric(y)
  return(y)
}



